/* Declare function to change language accordingly */

const changeLanguage = (evt) => {
  const lang = evt.target.id;
  let targetObj = objEnglish;
  if (lang === 'portuguese') targetObj = objPortuguese;

  localStorage.setItem('clientLanguage', lang);

  Object.entries(targetObj.class).forEach((el) => {
    const HTMLElement = document.getElementsByClassName(el[0])[0];

    HTMLElement.innerHTML = el[1];
    if (localStorage.getItem('darkmode') === 'true') {
      
      for (child of HTMLElement.children) {
        switchText(child)
      };
    }
  });

  Object.entries(targetObj.list).forEach((list) => {
    const HTMLList = document.getElementsByClassName(list[0])[0];

    list[1].forEach((text, index) => {
      const listEl = HTMLList.children[index];
      listEl.innerHTML = text;
    });
  });
};

/* Change language according to localStorage on page load */

const localLanguage = localStorage.getItem('clientLanguage');

window.addEventListener('load', () => {
  if (localLanguage !== null) {
    const languageRadio = document.getElementById(localLanguage);
    languageRadio.checked = true;

    const simObject = {
      target: languageRadio,
    };

    changeLanguage(simObject);
  } else {
    const languageRadio = document.getElementById('english');
    languageRadio.checked = true;
  }
});

/* Change Language on Click language radio */

const languageRadio = document.getElementsByName('language');

languageRadio.forEach((btn) => {
  btn.addEventListener('click', changeLanguage);
})